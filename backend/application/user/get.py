from flask import Blueprint, request, jsonify
from math import ceil
from ..tools import get_session, user_schema, access_pass
from ..postgres import db_close, db_open


bp = Blueprint("user_get", __name__)


@bp.get("/user/<key>")
def get(key):
    con, cur = db_open()

    session = get_session(cur)
    if session["status"] != 200:
        db_close(con, cur)
        return jsonify(session)

    cur.execute("""
        SELECT
            "user".*,
            CASE WHEN block.user_key IS NOT NULL
                THEN true ELSE false END AS blocked
        FROM "user"
        LEFT JOIN block ON "user".key = block.user_key
        WHERE "user".key::TEXT = %s OR "user".username = %s;
    """, (key, key))
    user = cur.fetchone()

    if not user:
        db_close(con, cur)
        return jsonify({
            "status": 404,
            "error": "Oops! The user you're looking for doesn't exist"
        })

    _access = {}
    for x in access_pass:
        if x not in _access:
            _access[x] = {}
            for y in access_pass[x]:
                if y[1] not in _access[x]:
                    _access[x][y[1]] = []
                _access[x][y[1]].append(y[0])

    db_close(con, cur)
    return jsonify({
        "status": 200,
        "user": user_schema(user),
        "access": _access
    })


@bp.get("/users")
def get_many():
    con, cur = db_open()

    session = get_session(cur, True)
    if session["status"] != 200:
        db_close(con, cur)
        return jsonify(session)

    if "user:view" not in session["user"]["access"]:
        db_close(con, cur)
        return jsonify({
            "status": 400,
            "error": "unauthorized access"
        })

    searchParams = {
        "search": "",
        "status": "active",
        "order": "latest",
        "page_no": 1,
        "page_size": 24
    }
    search = request.args.get("search", searchParams["search"]).strip()
    status = request.args.get("status", searchParams["status"])
    order = request.args.get("order", searchParams["order"])
    page_no = int(request.args.get("page_no", searchParams["page_no"]))
    page_size = int(request.args.get("page_size", searchParams["page_size"]))

    order_by = {
        'latest': 'date_created',
        'oldest': 'date_created',
        'name (a-z)': 'name',
        'name (z-a)': 'name'
    }
    order_dir = {
        'latest': 'DESC',
        'oldest': 'ASC',
        'name (a-z)': 'ASC',
        'name (z-a)': 'DESC'
    }

    cur.execute(f"""
        SELECT
            "user".*,
            CASE WHEN block.user_key IS NOT NULL
                THEN true ELSE false END AS blocked,
            COUNT(*) OVER() AS _count
        FROM "user"
        LEFT JOIN block ON "user".key = block.user_key
        WHERE (
                %s = 'all' OR "user".status = %s
            ) AND (
                %s = ''
                OR CONCAT_WS(', ', "user".key, "user".name, "user".email
            ) ILIKE %s
        )
        ORDER BY {order_by[order]} {order_dir[order]}
        LIMIT %s OFFSET %s;
    """, (
        status, status,
        search, f"%{search}%",
        page_size, (page_no - 1) * page_size
    ))
    users = cur.fetchall()

    db_close(con, cur)
    return jsonify({
        "status": 200,
        "users": [user_schema(x) for x in users],
        "total_page": ceil(users[0]["_count"] / page_size) if users else 0,
        "order_by": list(order_by.keys()),
        "searchParams": searchParams,
        "_status": ['anonymous', 'signedup', 'active'],
    })


@bp.get("/users/admin")
def get_admins():
    con, cur = db_open()

    session = get_session(cur, True)
    if session["status"] != 200:
        db_close(con, cur)
        return jsonify(session)
    user = session["user"]

    if "user:set_access" not in user["access"]:
        db_close(con, cur)
        return jsonify({
            "status": 400,
            "error": "unauthorized access"
        })

    searchParams = {
        "entity_type": "all",
        "action": "all",
        "search": "",
        "order": "latest",
        "page_no": 1,
        "page_size": 24
    }
    entity_type = request.args.get(
        "entity_type", searchParams["entity_type"]).strip()
    action = request.args.get("action", searchParams["action"]).strip()
    search = request.args.get("search", searchParams["search"]).strip()
    order = request.args.get("order", searchParams["order"]).strip()
    page_no = int(request.args.get("page_no", searchParams["page_no"]))
    page_size = int(request.args.get("page_size", searchParams["page_size"]))

    order_by = {
        'latest': '"user".date_created',
        'oldest': '"user".date_created',
        'name (a-z)': '"user".name',
        'name (z-a)': '"user".name'
    }
    order_dir = {
        'latest': 'DESC',
        'oldest': 'ASC',
        'name (a-z)': 'ASC',
        'name (z-a)': 'DESC'
    }

    cur.execute(f"""
        SELECT
            "user".*,
            CASE WHEN block.user_key IS NOT NULL
                THEN true ELSE false END AS blocked,
            COUNT(*) OVER() AS _count
        FROM "user"
        LEFT JOIN block ON "user".key = block.user_key
        WHERE
            array_length("user".access, 1) IS NOT NULL
            AND (%s = '' OR CONCAT_WS(
                ', ', "user".key, "user".name, "user".email) ILIKE %s)
            AND (%s = 'all' OR ARRAY_TO_STRING("user".access, ',') ILIKE %s)
            AND (%s = 'all' OR ARRAY_TO_STRING("user".access, ',') ILIKE %s)
        ORDER BY {order_by[order]} {order_dir[order]}
        LIMIT %s OFFSET %s;
    """, (
        search, f"%{search}%",
        entity_type, f"%{entity_type}:%",
        action, f"%{entity_type}:{action}%",
        page_size, (page_no - 1) * page_size
    ))
    users = cur.fetchall()

    _access = {
        "all": ['all']
    }
    for x in access_pass:
        if x not in _access:
            _access[x] = ["all"]
            for y in access_pass[x]:
                _access[x].append(y[0])

    db_close(con, cur)
    return jsonify({
        "status": 200,
        "users": [user_schema(x) for x in users],
        "total_page": ceil(users[0]["_count"] / page_size) if users else 0,
        "order_by": list(order_by.keys()),
        "searchParams": searchParams,
        "search_query": _access,
    })
